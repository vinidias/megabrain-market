import { readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourcePath = resolve(root, 'shared/bootstrap-tier-keys.js');
const generatedPath = resolve(root, 'api/_bootstrap-tier-keys.js');
const check = process.argv.includes('--check');

const header = `// AUTO-GENERATED by scripts/sync-bootstrap-tier-keys.mjs. DO NOT EDIT.\n// Source: shared/bootstrap-tier-keys.js\n\n`;
const source = await readFile(sourcePath, 'utf8');
const expected = `${header}${source}`;

if (check) {
  const actual = await readFile(generatedPath, 'utf8').catch(() => '');
  if (actual !== expected) {
    console.error('api/_bootstrap-tier-keys.js is stale. Run npm run sync:bootstrap-tier-keys.');
    process.exitCode = 1;
  }
} else {
  await writeFile(generatedPath, expected);
  console.log('Updated api/_bootstrap-tier-keys.js');
}
